package com.bptn.course.week1;

public class ASCIIExample {

	public static void main(String[] args) {
		char c = 'D';
		int ascii = c;
		System.out.println("The ASCII value of " + c + " is: " + ascii);
		
		int count = 1;
		while (count <= 10) {
		    count *= 2;
		}
		count = count - 10;
		System.out.println(count);

	}

}
